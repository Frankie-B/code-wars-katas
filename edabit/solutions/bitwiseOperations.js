// Write three functions to calculate the bitwise AND, bitwise OR and bitwise XOR of two numbers.

function bitwiseAND(n1, n2) {
	return n1 & n2;
}

function bitwiseOR(n1, n2) {
	return n1 | n2;
}

function bitwiseXOR(n1, n2) {
	return n1 ^ n2;
}